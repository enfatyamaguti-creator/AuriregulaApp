export function getProtocoloPorSlug(slug: string): Protocolo | undefined {
  return PROTOCOLOS.find(p => p.slug === slug);
}

export function getProtocolosPorGrupo(grupo: string): Protocolo[] {
  if (grupo === 'todos') return PROTOCOLOS;
  return PROTOCOLOS.filter(p => p.grupo === grupo);
}

export function getProtocolosPorPopulacao(populacao: string): Protocolo[] {
  return PROTOCOLOS.filter(p => p.populacao === populacao);
}

export function buscarProtocolos(termo: string): Protocolo[] {
  const t = termo.toLowerCase().trim();
  if (!t) return PROTOCOLOS;
  return PROTOCOLOS.filter(p =>
    p.nome.toLowerCase().includes(t) ||
    p.indicacao.toLowerCase().includes(t) ||
    p.pontos.some(pt => pt.toLowerCase().includes(t)) ||
    p.sinais.some(s => s.toLowerCase().includes(t))
  );
}

